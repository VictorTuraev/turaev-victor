import graphics as gr
window=gr.GraphWin("gfd",500,500)
sun=gr.Circle(gr.Point(100,100),30)
l2=gr.Rectangle(gr.Point(0,0),gr.Point(500,250))
l2.setFill("blue")
l2.draw(window)
sun.setFill("yellow")
line=gr.Line(gr.Point(0,250),gr.Point(500,250))
l1=gr.Rectangle(gr.Point(0,250),gr.Point(500,500))
l1.setFill("green")
l1.draw(window)
hbase=gr.Rectangle(gr.Point(150,300),gr.Point(250,150))
hbase.setFill('yellow')
hbase.draw(window)
sun.draw(window)
line.draw(window)
window.getMouse()
window.close()
